# ── Main.cf template ───────────────────────────────────────────────────────


def render_postfix_main_cf(profile: SetupProfile) -> str:
    """Generate /etc/postfix/main.cf from a SetupProfile.

    Produces a config that is:
      - Chrooted (Debian default)
      - TLS everywhere (25/465/587 all enforce TLS)
      - Rspamd milter for spam/virus filtering
      - Postscreen for pre-queue zombie blocking
      - Rate-limited via anvil
      - Virtual domains from the profile
    """
    hostname = profile.hostname
    mynetworks = ["127.0.0.0/8", "[::ffff:127.0.0.0]/104", "[::1]/128"]
    if profile.public_ipv4:
        mynetworks.append(profile.public_ipv4 + "/32")

    lines = [
        "# KTC Mail — Postfix main.cf",
        "# Generated by ktc-mail config-renderer. DO NOT EDIT MANUALLY.",
        f"# Source: {profile.domain} via {__file__}",
        "",
        "# ── Base identity ────────────────────────────────────────────",
        f"myhostname = {hostname}",
        f"mydomain = {profile.domain}",
        f"myorigin = $mydomain",
        f"mydestination = $myhostname, localhost.$mydomain, localhost",
        f"relay_domains = $mydomain",
        "",
        "# ── Networks ────────────────────────────────────────────────",
        f"mynetworks = {' '.join(mynetworks)}",
        f"inet_interfaces = all",
        f"inet_protocols = {'all' if profile.has_ipv6 else 'ipv4'}",
        "",
        "# ── TLS (enforced everywhere) ───────────────────────────────",
        "smtpd_tls_security_level = may",
        "smtpd_tls_auth_only = yes",
        "smtpd_tls_protocols = !SSLv2, !SSLv3, !TLSv1, !TLSv1.1",
        "smtpd_tls_mandatory_protocols = !SSLv2, !SSLv3, !TLSv1, !TLSv1.1",
        "smtpd_tls_mandatory_ciphers = high",
        "smtpd_tls_eecdh_grade = strong",
        "smtpd_tls_key_file = /etc/letsencrypt/live/ktc-mail/privkey.pem",
        "smtpd_tls_cert_file = /etc/letsencrypt/live/ktc-mail/fullchain.pem",
        "smtpd_tls_CAfile = /etc/letsencrypt/live/ktc-mail/chain.pem",
        "smtpd_tls_session_cache_database = btree:${data_directory}/smtpd_scache",
        "smtpd_tls_received_header = yes",
        "",
        "smtp_tls_security_level = may",
        "smtp_tls_protocols = !SSLv2, !SSLv3, !TLSv1, !TLSv1.1",
        "smtp_tls_mandatory_protocols = !SSLv2, !SSLv3, !TLSv1, !TLSv1.1",
        "smtp_tls_session_cache_database = btree:${data_directory}/smtp_scache",
        "",
        "# ── SASL auth (via Dovecot) ────────────────────────────────",
        "smtpd_sasl_type = dovecot",
        "smtpd_sasl_path = private/auth",
        "smtpd_sasl_auth_enable = yes",
        "smtpd_sasl_security_options = noanonymous",
        "smtpd_sasl_tls_security_options = noanonymous",
        "broken_sasl_auth_clients = yes",
        "",
        "# ── Rspamd milter ──────────────────────────────────────────",
        "smtpd_milters = inet:localhost:11332",
        "non_smtpd_milters = inet:localhost:11332",
        "milter_protocol = 6",
        "milter_mail_macros = i {mail_addr} {client_addr} {client_name} {auth_authen}",
        "milter_default_action = accept",
        "",
        "# ── Postscreen (pre-queue filtering) ───────────────────────",
        "postscreen_access_list = permit_mynetworks",
        "postscreen_dnsbl_sites = zen.spamhaus.org, bl.mailspike.net, b.barracudacentral.org",
        "postscreen_dnsbl_threshold = 3",
        "postscreen_greet_action = enforce",
        "postscreen_bare_newline_action = enforce",
        "postscreen_non_smtp_command_action = enforce",
        "postscreen_pipelining_action = enforce",
        "",
        "# ── Rate limits (anvil) ────────────────────────────────────",
        "anvil_rate_time_unit = 60s",
        "smtpd_client_connection_rate_limit = 30",
        "smtpd_client_connection_count_limit = 10",
        "smtpd_client_message_rate_limit = 60",
        "smtpd_client_recipient_rate_limit = 60",
        "smtpd_client_event_limit_exceptions = ${mynetworks}",
        "",
        "# ── Restrictions ───────────────────────────────────────────",
        "smtpd_helo_required = yes",
        "disable_vrfy_command = yes",
        "strict_rfc821_envelopes = yes",
        "",
        "# Relay control: Postfix 3.7+ requires this or smtpd_recipient_restrictions",
        "# to have at least reject_unauth_destination — without it smtpd refuses to start.",
        "smtpd_relay_restrictions = permit_mynetworks, permit_sasl_authenticated, reject_unauth_destination",
        "",
        "# ── Queue ───────────────────────────────────────────────────",
        "maximal_queue_lifetime = 4d",
        "bounce_queue_lifetime = 4d",
        "minimal_backoff_time = 300s",
        "maximal_backoff_time = 3600s",
        "queue_run_delay = 300s",
        "",
        "# ── Delivery ─────────────────────────────────────────────────",
        "default_transport = smtp",
        "relay_transport = relay",
        "# Virtual domain delivery via Dovecot LMTP",
        "virtual_transport = lmtp:unix:private/dovecot-lmtp",
        "virtual_mailbox_domains = $mydomain",
        # Virtual alias and mailbox maps: Postfix queries these to
        # decide whether a recipient is valid. Dovecot handles the
        # actual storage. The maps are managed by ktc-mail later.
        "virtual_alias_maps = texthash:/etc/postfix/virtual_alias",
        "virtual_mailbox_maps = texthash:/etc/postfix/virtual_mbx",
        "",
        "# ── Message size ────────────────────────────────────────────",
        "message_size_limit = 25600000",
        "mailbox_size_limit = 0",
        "",
    ]
    return "\n".join(lines) + "\n"


def render_postfix_master_cf(profile: SetupProfile) -> str:
    """Generate /etc/postfix/master.cf.

    Defines the smtpd, submission (587), and smtps (465) services.
    Postscreen runs before smtpd as the first line of defence.
    """
    lines = [
        "# KTC Mail — Postfix master.cf",
        "# Generated by ktc-mail config-renderer. DO NOT EDIT MANUALLY.",
        "",
        "# ── SMTP (port 25, with postscreen) ───────────────────────",
        "smtp      inet  n       -       n       -       -       smtpd",
        "  -o syslog_name=postfix/smtpd",
        "  -o smtpd_tls_security_level=may",
        "",
        "# ── Submission (port 587, STARTTLS required) ──────────────",
        "submission inet n       -       n       -       -       smtpd",
        "  -o syslog_name=postfix/submission",
        "  -o smtpd_tls_security_level=encrypt",
        "  -o smtpd_sasl_auth_enable=yes",
        "  -o smtpd_tls_auth_only=yes",
        '  -o smtpd_reject_unlisted_recipient=no',
        "  -o smtpd_client_restrictions=permit_sasl_authenticated,reject",
        "  -o milter_macro_daemon_name=ORIGINATING",
        "",
        "# ── SMTPS (port 465, implicit TLS) ────────────────────────",
        "smtps     inet  n       -       n       -       -       smtpd",
        "  -o syslog_name=postfix/smtps",
        "  -o smtpd_tls_wrappermode=yes",
        "  -o smtpd_sasl_auth_enable=yes",
        '  -o smtpd_reject_unlisted_recipient=no',
        "  -o smtpd_client_restrictions=permit_sasl_authenticated,reject",
        "  -o milter_macro_daemon_name=ORIGINATING",
        "",
        "# ── Postscreen (pre-queue on port 25) ─────────────────────",
        "127.0.0.1:12525 inet n  n       y       -       -       postscreen",
        "  -o syslog_name=postfix/postscreen",
        "  -o postscreen_cache_map=btree:${data_directory}/postscreen_cache",
        "",
        "# ── Internal services ──────────────────────────────────────",
        "pickup    fifo  n       -       y       60      1       pickup",
        "cleanup   unix  n       -       y       -       0       cleanup",
        "qmgr      fifo  n       -       n       300     1       qmgr",
        "rewrite   unix  -       -       y       -       -       trivial-rewrite",
        "bounce    unix  -       -       y       -       0       bounce",
        "defer     unix  -       -       y       -       0       bounce",
        "trace     unix  -       -       y       -       0       bounce",
        "verify    unix  -       -       y       -       1       verify",
        "flush     unix  n       -       y       1000?   0       flush",
        "proxymap  unix  -       -       n       -       -       proxymap",
        "smtp      unix  -       -       y       -       -       smtp",
        "relay     unix  -       -       y       -       -       smtp",
        "showq     unix  n       -       y       -       -       showq",
        "error     unix  -       -       y       -       -       error",
        "retry     unix  -       -       y       -       -       error",
        "discard   unix  -       -       y       -       -       discard",
        "lmtp      unix  -       -       y       -       -       lmtp",
        "anvil     unix  -       -       y       -       1       anvil",
        "scache    unix  -       -       y       -       1       scache",
        "",
        "# ── Dovecot LMTP delivery ─────────────────────────────────",
        "dovecot   unix  -       n       n       -       -       pipe",
        "  flags=DRhu user=vmail argv=/usr/lib/dovecot/deliver",
        '  -f ${sender} -d ${recipient}',
        "",
    ]
    return "\n".join(lines) + "\n"


# ── Dovecot config ────────────────────────────────────────────────────────


def render_dovecot_conf(profile: SetupProfile) -> str:
    """Generate /etc/dovecot/dovecot.conf.

    Configures:
      - IMAPS on port 993 (no cleartext IMAP)
      - LMTP for Postfix delivery
      - Virtual mailboxes in /var/mail/%d/%n
      - SASL auth for Postfix
      - Sieve for server-side filtering
    """
    return f"""# KTC Mail — Dovecot config
# Generated by ktc-mail config-renderer. DO NOT EDIT MANUALLY.
# Source: {profile.domain}

# ── Protocols ──────────────────────────────────────────────
protocols = imap lmtp sieve

# ── Listen ─────────────────────────────────────────────────
listen = *

# ── Base directory ─────────────────────────────────────────
base_dir = /var/run/dovecot/

# ── Authentication ─────────────────────────────────────────
auth_username_format = %Lu
auth_mechanisms = plain login

# PAM auth for system users (admin access)
userdb {{
  driver = passwd
}}

# Virtual mailbox auth via passwd-file
passdb {{
  driver = passwd-file
  args = /etc/dovecot/passwd
}}

userdb {{
  driver = static
  args = uid=vmail gid=vmail home=/var/mail/%d/%n
}}

# SASL for Postfix
service auth {{
  unix_listener /var/spool/postfix/private/auth {{
    mode = 0660
    user = postfix
    group = postfix
  }}
  unix_listener auth-userdb {{
    mode = 0600
    user = vmail
  }}
}}

# ── TLS ────────────────────────────────────────────────────
ssl = required
ssl_cert = </etc/letsencrypt/live/ktc-mail/fullchain.pem
ssl_key = </etc/letsencrypt/live/ktc-mail/privkey.pem
ssl_min_protocol = TLSv1.2
ssl_cipher_list = ECDHE-ECDSA-AES128-GCM-SHA256:ECDHE-RSA-AES128-GCM-SHA256:ECDHE-ECDSA-AES256-GCM-SHA384:ECDHE-RSA-AES256-GCM-SHA384
ssl_prefer_server_ciphers = yes

# ── Mail location ──────────────────────────────────────────
mail_location = maildir:/var/mail/%d/%n
mail_privileged_group = mail
mail_access_groups = vmail

# ── Namespaces ─────────────────────────────────────────────
namespace inbox {{
  inbox = yes
  separator = /
  mailbox Drafts {{
    auto = subscribe
    special_use = \\Drafts
  }}
  mailbox Junk {{
    auto = subscribe
    special_use = \\Junk
  }}
  mailbox Trash {{
    auto = subscribe
    special_use = \\Trash
  }}
  mailbox Sent {{
    auto = subscribe
    special_use = \\Sent
  }}
}}

# ── LMTP (delivery from Postfix) ───────────────────────────
service lmtp {{
  unix_listener /var/spool/postfix/private/dovecot-lmtp {{
    mode = 0600
    user = postfix
    group = postfix
  }}
}}

# ── IMAPS (port 993 only — no cleartext) ───────────────────
service imap-login {{
  inet_listener imap {{
    port = 0  # disabled
  }}
  inet_listener imaps {{
    port = 993
    ssl = yes
  }}
}}

# ── Sieve (server-side filtering) ─────────────────────────
plugin {{
  sieve = /var/mail/%d/%n/.dovecot.sieve
  sieve_default = /var/lib/dovecot/sieve/default.sieve
  sieve_dir = /var/mail/%d/%n/sieve
}}

protocol lda {{
  mail_plugins = $mail_plugins sieve
}}

protocol lmtp {{
  mail_plugins = $mail_plugins sieve
}}

# ── Service config ─────────────────────────────────────────
service imap {{
  process_limit = 256
  client_limit = 1
}}

# ── Logging ────────────────────────────────────────────────
auth_verbose = yes
auth_verbose_passwords = sha1
mail_debug = no
"""


# ── Rspamd config ────────────────────────────────────────────────────────


def render_rspamd_worker_conf(profile: SetupProfile) -> str:
    """Generate rspamd local.d/worker-proxy.inc (milter mode).

    Written to /etc/rspamd/local.d/worker-proxy.inc — merges with the
    default rspamd_proxy worker defined in rspamd.conf.
    The named upstream block matches the default upstream "local" syntax.
    """
    return f"""# KTC Mail — Rspamd proxy worker override
# Generated by ktc-mail config-renderer. DO NOT EDIT MANUALLY.
# Written to local.d/worker-proxy.inc — merges with the default
# rspamd_proxy worker defined in rspamd.conf.

# Spawn 4 milter workers instead of the default 1
count = 4;

# Self-scan mode: the proxy worker itself runs the spam scanner
# instead of forwarding to a separate rspamd instance
upstream "local" {{
  default = yes;
  self_scan = yes;
}}
"""


def render_rspamd_controller_conf(profile: SetupProfile) -> str:
    """Generate rspamd local.d/worker-controller.inc.

    Merges with the default controller worker to restrict admin
    access to localhost only.
    """
    return f"""# KTC Mail — Rspamd controller worker override
# Generated by ktc-mail config-renderer. DO NOT EDIT MANUALLY.

secure_ip = "127.0.0.1";
"""


