def render_all(profile: SetupProfile) -> dict[str, str]:
    """Render all mail stack configuration files.

    Returns a dict of relative_path → config_content.
    The caller decides where to write them.
    """
    result: dict[str, str] = {}
    for relpath, (_desc, func_name, _mode) in RENDERED_FILES.items():
        render_fn = globals()[func_name]
        result[relpath] = render_fn(profile)
    return result


def write_all(
    profile: SetupProfile,
    dest: Path | str = Path("/etc"),
    dry_run: bool = False,
) -> dict[str, Path]:
    """Write all rendered config files under dest/.

    Args:
        profile: The setup profile to render.
        dest: Root directory (default /etc — for real deployment).
        dry_run: Print paths instead of writing.

    Returns:
        Dict of relative_path → absolute Path for written files.
    """
    dest = Path(dest)
    rendered = render_all(profile)
    written: dict[str, Path] = {}

    for relpath, content in rendered.items():
        full_path = dest / relpath
        if dry_run:
            print(f"dry-run: {full_path} ({len(content)} bytes)")
            written[relpath] = full_path
            continue
        full_path.parent.mkdir(parents=True, exist_ok=True)
        full_path.write_text(content, encoding="utf-8")
        written[relpath] = full_path

    return written


# ── DKIM key generation ──────────────────────────────────────────────────


def dkim_generate(
    domain: str,
    selector: str = "default",
    bits: int = 2048,
) -> tuple[bytes, str]:
    """Generate DKIM keypair and DNS record.

    Shells out to openssl — the canonical tool for RSA key generation.
    No Python crypto library dependency. No reimplementation of RSA.

    Returns:
        (private_key_pem, dns_txt_record_string)

    The private key is returned as PEM bytes — caller decides where to
    write it. The DNS record is a ready-to-publish TXT value.
    """
    priv = subprocess.run(
        ["openssl", "genrsa", str(bits)],
        capture_output=True, check=True,
    ).stdout

    pub_der = subprocess.run(
        ["openssl", "rsa", "-pubin", "-outform", "DER"],
        input=subprocess.run(
            ["openssl", "rsa", "-pubout"],
            input=priv, capture_output=True, check=True,
        ).stdout,
        capture_output=True, check=True,
    ).stdout

    b64 = base64.b64encode(pub_der).decode()
    record = f"v=DKIM1; k=rsa; p={b64}"
    return priv, record


def dkim_write(args: argparse.Namespace) -> int:
    """CLI handler for `ktc-mail dkim generate`."""
    from .config import DKIM_DIR, read_json, SetupProfile

    data = read_json(args.config)
    profile = SetupProfile.from_dict(data)
    selector = args.selector

    priv_pem, dns_record = dkim_generate(profile.domain, selector)

    if args.dry_run:
        print(f"dry-run: would write key to {DKIM_DIR / selector}.private")
        print(f"dry-run: DNS TXT \"{selector}._domainkey.{profile.domain}\"")
        print(dns_record)
        return 0

    path = DKIM_DIR / f"{selector}.private"
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_bytes(priv_pem)
    path.chmod(0o600)

    print(f"wrote: {path}")
    print()
    print(f"DNS TXT record for {selector}._domainkey.{profile.domain}:")
    print(dns_record)
    return 0


# ── MTA-STS policy file ──────────────────────────────────────────────────


def render_mta_sts_policy(profile: SetupProfile) -> str:
    """Generate MTA-STS policy file.

    Served at https://mta-sts.{domain}/.well-known/mta-sts.txt
    """
    return f"""version: STSv1
mode: enforce
mx: {profile.hostname}
max_age: 604800
"""


# ── CLI entry point ──────────────────────────────────────────────────────


def main() -> int:
    parser = argparse.ArgumentParser(
        description="KTC Mail config renderer (Phase 3)",
    )
    parser.add_argument(
        "command",
        choices=("render", "write"),
        help="render=print to stdout, write=deploy to /etc",
    )
    parser.add_argument("--config", type=Path, default=SETUP_PATH)
    parser.add_argument("--dest", type=Path, default=Path("/etc"))
    parser.add_argument("--dry-run", action="store_true",
                        help="Show what would be written")
    args = parser.parse_args()

    try:
        if not args.config.exists():
            print(f"setup profile not found: {args.config}", file=sys.stderr)
            return 1

        data = read_json(args.config)
        profile = SetupProfile.from_dict(data)

        if args.command == "render":
            rendered = render_all(profile)
            for relpath, content in rendered.items():
                print(f"# {'=' * 60}")
                print(f"# {relpath}")
                print(f"# {'=' * 60}")
                print(content, end="")
            return 0

        if args.command == "write":
            written = write_all(profile, dest=args.dest, dry_run=args.dry_run)

            # Also write MTA-STS
            mta_sts = render_mta_sts_policy(profile)
            sts_path = args.dest / "nginx/mta-sts.txt"
            if args.dry_run:
                print(f"dry-run: {sts_path}")
            else:
                sts_path.parent.mkdir(parents=True, exist_ok=True)
                sts_path.write_text(mta_sts, encoding="utf-8")
                written["nginx/mta-sts.txt"] = sts_path

            if args.dry_run:
                return 0

            count = len(written)
            print(f"wrote {count} config files under {args.dest}")
            for relpath, path in sorted(written.items()):
                print(f"  {path}")
            return 0

        return 1

    except Exception as exc:
        print(f"config error: {exc}", file=sys.stderr)
        return 1


if __name__ == "__main__":
    sys.exit(main())
