#!/usr/bin/env python3
"""KTC Mail — mail stack configuration renderer.

Phase 3 deliverable. Takes a SetupProfile and generates working
Postfix, Dovecot, Rspamd, and Nginx configuration files.

This is the module that actually makes this a mail server instead
of just a DNS setup wizard. Every template is a function that takes
a SetupProfile and returns a config string. No side effects in the
render functions — the caller decides where to write.

Design rules:
  - Templates produce VALID config for Debian/Ubuntu packages
  - TLS is mandatory everywhere (STARTTLS on 25/587, TLS on 465/993)
  - Rspamd milter is inline (not proxy) — reject at SMTP time
  - Virtual mailboxes via Dovecot LMTP, stored in /var/mail/%d/%n
"""

