# ── Nginx vhost ──────────────────────────────────────────────────────────


def render_nginx_webmail_vhost(profile: SetupProfile) -> str:
    """Generate Nginx vhost for webmail and admin endpoints.

    Proxies to the webmail backend (port 5085 default).
    Serves ACME webroot for HTTP-01 challenges when enabled.
    """
    acme_location = ""
    if profile.certificate_mode == "http-01":
        acme_location = """
    location /.well-known/acme-challenge/ {
        root /var/lib/ktc-mail/acme-webroot;
    }
"""

    return f"""# KTC Mail — Nginx vhost (webmail + admin)
# Generated by ktc-mail config-renderer. DO NOT EDIT MANUALLY.

server {{
    listen 80;
    listen [::]:80;
    server_name {profile.hostname} {profile.webmail_host} {profile.admin_host};
    return 301 https://$server_name$request_uri;
}}

server {{
    listen 443 ssl http2;
    listen [::]:443 ssl http2;
    server_name {profile.webmail_host};

    ssl_certificate /etc/letsencrypt/live/ktc-mail/fullchain.pem;
    ssl_certificate_key /etc/letsencrypt/live/ktc-mail/privkey.pem;
    ssl_protocols TLSv1.2 TLSv1.3;
    ssl_ciphers ECDHE-ECDSA-AES128-GCM-SHA256:ECDHE-RSA-AES128-GCM-SHA256:ECDHE-ECDSA-AES256-GCM-SHA384:ECDHE-RSA-AES256-GCM-SHA384;
    ssl_prefer_server_ciphers on;

    client_max_body_size 50M;

    location / {{
        proxy_pass http://127.0.0.1:5085;
        proxy_set_header Host $host;
        proxy_set_header X-Real-IP $remote_addr;
        proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;
        proxy_set_header X-Forwarded-Proto $scheme;
    }}
}}

server {{
    listen 443 ssl http2;
    listen [::]:443 ssl http2;
    server_name {profile.admin_host};

    ssl_certificate /etc/letsencrypt/live/ktc-mail/fullchain.pem;
    ssl_certificate_key /etc/letsencrypt/live/ktc-mail/privkey.pem;
    ssl_protocols TLSv1.2 TLSv1.3;
    ssl_ciphers ECDHE-ECDSA-AES128-GCM-SHA256:ECDHE-RSA-AES128-GCM-SHA256:ECDHE-ECDSA-AES256-GCM-SHA384:ECDHE-RSA-AES256-GCM-SHA384;
    ssl_prefer_server_ciphers on;

    location / {{
        proxy_pass http://127.0.0.1:5085;
        proxy_set_header Host $host;
        proxy_set_header X-Real-IP $remote_addr;
        proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;
        proxy_set_header X-Forwarded-Proto $scheme;
    }}
}}
"""


